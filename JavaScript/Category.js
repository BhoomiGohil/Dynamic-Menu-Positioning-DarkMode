function category() {
  var block = document.querySelector(".category").style.display === "flex";
  if (block) {
    document.querySelector(".category").style.display = "none";
  } else {
    document.querySelector(".category").style.display = "flex";
  }
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

function categoryfilter() {
  document.querySelector(".category").style.display = "none";
}
